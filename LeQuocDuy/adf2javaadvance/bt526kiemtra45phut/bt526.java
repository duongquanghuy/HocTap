    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtFullName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập Họ Tên\nVui lòng thử lại!");
        } else if (txtRollNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập Roll No!\nVui lòng thử lại!");
        } else {
            String fullName = txtFullName.getText();
            String gender = txtGender.getText();
            String rollNo = txtRollNo.getText();
            String email = txtEmail.getText();
            String address = txtAddress.getText();

            SinhVien sv = new SinhVien(fullName, gender, rollNo, email, address);

            nhapSinhVien(sv);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

