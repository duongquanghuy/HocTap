    private void nhapSinhVien(SinhVien sv) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/quanlysinhvien?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC", "root", "");
            
            rollNoList.clear();
            String sql2 = "select rollno from students";
            Statement statement2 = conn.createStatement();
            ResultSet result = statement2.executeQuery(sql2);

            while (result.next()) {
                rollNoList.add(result.getString("rollno"));
            }

            String st = txtRollNo.getText();
            boolean check = false;
            
            for (String rl : rollNoList) {
                if (rl.equals(st)) {
                    JOptionPane.showMessageDialog(rootPane, "Đã có Roll No này trong hệ thống!\nVui lòng nhập Roll No khác!");
                    check = true;
                }
            }
            
            if (check == false) {
                String sql = "insert into students(rollno, fullname, gender, email, address) values(?, ?, ?, ?, ?)";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, sv.getRollNo());
                statement.setString(2, sv.getFullName());
                statement.setString(3, sv.getGender());
                statement.setString(4, sv.getEmail());
                statement.setString(5, sv.getAddress());

                statement.execute();

                statement.close();
                conn.close();

                txtFullName.setText("");
                txtGender.setText("");
                txtRollNo.setText("");
                txtEmail.setText("");
                txtAddress.setText("");
            }
            
            statement2.close();
            conn.close();

        } catch (Exception ex) {
            Logger.getLogger(bt526.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void timSinhVien(String rn) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/quanlysinhvien?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC", "root", "");

            String sql = "select * from students where rollno = " + rn;
            Statement statement = conn.createStatement();
            ResultSet result = statement.executeQuery(sql);

            if (result.next()) {
                txtFullName.setText(result.getString("fullname"));
                txtGender.setText(result.getString("gender"));
                txtRollNo.setText(result.getString("rollno"));
                txtEmail.setText(result.getString("email"));
                txtAddress.setText(result.getString("address"));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Không tìm thấy sinh viên có Roll No là: " + rn + "\nVui lòng thử lại!");
            }

            statement.close();
            conn.close();
        } catch (Exception ex) {
            Logger.getLogger(bt526.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

